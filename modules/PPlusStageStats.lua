local p = {}

function p.rows()
	local rows = {}
	
    for _, stageObj in ipairs(data) do
        local tr = mw.html.create('tr')
        
        local function subtractionCell(p1, p2)
        	if type(p1) == "number" and type(p2) == "number" then
        		tr:tag('td'):wikitext(math.floor(p1 - p2))
        	else
        		tr:tag("td")
        	end
        end

        tr:tag('td'):wikitext(stageObj.name)
        subtractionCell(stageObj.ledgeR_X, stageObj.ledgeL_X)
        subtractionCell(stageObj.dead0_Y, stageObj.ledgeL_Y)
        subtractionCell(stageObj.dead1_X, stageObj.ledgeR_X)
        subtractionCell(stageObj.ledgeL_Y, stageObj.dead1_Y)
        subtractionCell(stageObj.lowPlat_Y, stageObj.ledgeL_Y)
        subtractionCell(stageObj.highPlat_Y, stageObj.ledgeL_Y)
        subtractionCell(stageObj.platRight_X, stageObj.platLeft_X)
		tr:tag("td"):wikitext(stageObj.notes)
        
        table.insert(rows, tostring(tr))
    end

	return table.concat(rows)
end

return p